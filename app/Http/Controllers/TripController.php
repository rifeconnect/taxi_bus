    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //dd($request)->all();
        $userid = Auth::id();
        $trip = new trip;
        $trip->user_id = $userid;
        $trip->vehicle_driver_id = $request->vehicle_driver;
        $trip->departure_state_id = $request->departure_state;
        $trip->departureDate =$request->departure_date;
        $trip->arrival_state_id = $request->arrival_state;
        $trip->trip_price = $request->trip_price;
        $trip->save();
        session()->flash('trip_created', 'Trip was created successfully.');
        return back();
    }

